/* Editor theme + design switcher.
 *
 * Two orthogonal axes drive the editor chrome:
 *
 *   1. THEME  — System / Light / Dark / Retro. The user's brightness
 *      mode preference. Persists at localStorage["editor.theme"].
 *      Changing the theme pill always resets the chrome to that
 *      built-in look (see (3) below for why).
 *
 *   2. DESIGN — null | violet | mono | paper | espresso | coral |
 *      ember | vinyl | scarlet | glacier | phosphor | hornet | prism.
 *      An optional decorative palette inspired by various design
 *      systems. Persists at localStorage["editor.design"]. When set,
 *      the design wins visually — the theme axis still has a value
 *      underneath but is not what's painting the chrome.
 *
 * Both axes apply through a single `data-theme` attribute on <html>:
 * the resolved string is the design (if any), else the resolved theme.
 * CSS for built-ins lives at the bottom of index.css; CSS for designs
 * lives in styles/themes.css (generated by tools/gen-themes.mjs).
 *
 * (3) Why does picking a theme clear the design? A user clicking
 * "Light" expects to see Light. If the design "Violet" was overriding,
 * the click would be invisible — confusing. Resetting design when the
 * theme axis changes preserves the "click → see change" journey.
 *
 * Migration: older localStorage values "default" and the design names
 * stored under "editor.theme" are read transparently and split into
 * the right axis on first load. */

/** Theme axis values. Includes "system", which resolves at apply time. */
export type ThemePreference = "system" | "light" | "dark" | "retro";

/** Design axis values. */
export type Design =
  | "violet" | "mono" | "paper" | "espresso" | "coral" | "ember"
  | "vinyl" | "scarlet" | "glacier" | "phosphor" | "hornet" | "prism";

/** Resolved chrome — what `data-theme` actually gets set to. */
export type ResolvedTheme = "light" | "dark" | "retro" | Design;

/** Picker metadata — `swatch` is a 4-color preview ([bg, surface,
 *  brand, ink]). Used by Settings + foot-pill UIs. */
export type ThemeMeta = {
  name: ThemePreference;
  label: string;
  swatch: readonly [string, string, string, string];
};
export type DesignMeta = {
  name: Design;
  label: string;
  /** "light" or "dark" controls grouping in the design picker. */
  kind: "light" | "dark";
  swatch: readonly [string, string, string, string];
};

