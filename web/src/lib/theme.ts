const THEME_NAMES: ReadonlySet<ThemePreference> = new Set(themes.map((t) => t.name));
const DESIGN_NAMES: ReadonlySet<Design> = new Set(designs.map((d) => d.name));

const KEY_THEME = "editor.theme";
const KEY_DESIGN = "editor.design";
const EVENT_THEME = "editor-theme-change";
const EVENT_DESIGN = "editor-design-change";

function isTheme(v: unknown): v is ThemePreference {
  return typeof v === "string" && THEME_NAMES.has(v as ThemePreference);
}
function isDesign(v: unknown): v is Design {
  return typeof v === "string" && DESIGN_NAMES.has(v as Design);
}

function readStoredTheme(): ThemePreference {
  try {
    const saved = localStorage.getItem(KEY_THEME);
    if (isTheme(saved)) return saved;
    // Legacy migrations: "default" was the pre-Retro default; design
    // names stored under "editor.theme" come from the previous
    // single-axis implementation.
    if (saved === "default") return "system";
    if (isDesign(saved)) {
      // Adopt as a design and clear the theme key. The split happens
      // lazily so a user upgrading from the single-axis build sees
      // their existing "violet" still applied — just on a fresh
      // "system" theme underneath.
      try {
        localStorage.setItem(KEY_DESIGN, saved);
        localStorage.removeItem(KEY_THEME);
      } catch { /* ignore — we'll fall back to "system" */ }
      return "system";
    }
  } catch { /* localStorage may be blocked (sandboxed iframe, private mode). */ }
  return "system";
}

function readStoredDesign(): Design | null {
  try {
    const saved = localStorage.getItem(KEY_DESIGN);
    if (isDesign(saved)) return saved;
  } catch { /* see above */ }
  return null;
}

function prefersDark(): boolean {
  if (typeof window === "undefined" || !window.matchMedia) return false;
  return window.matchMedia("(prefers-color-scheme: dark)").matches;
}

function resolveTheme(pref: ThemePreference): "light" | "dark" | "retro" {
  if (pref === "system") return prefersDark() ? "dark" : "light";
  return pref;
}

function resolveActive(): ResolvedTheme {
  if (activeDesign) return activeDesign;
  return resolveTheme(activeTheme);
}

let activeTheme: ThemePreference = readStoredTheme();
let activeDesign: Design | null = readStoredDesign();

function applyAttr(resolved: ResolvedTheme) {
  if (typeof document === "undefined") return;
  if (resolved === "light") {
    document.documentElement.removeAttribute("data-theme");
  } else {
    document.documentElement.setAttribute("data-theme", resolved);
  }
}

applyAttr(resolveActive());

/** Set the theme axis. Picking a theme implicitly clears any active
 *  design — the click should always produce a visible change. */
export function setTheme(pref: ThemePreference) {
  const designCleared = activeDesign !== null;
  if (pref === activeTheme && !designCleared) return;
  activeTheme = pref;
  activeDesign = null;
  try {
    localStorage.setItem(KEY_THEME, pref);
    localStorage.removeItem(KEY_DESIGN);
  } catch { /* see above */ }
  applyAttr(resolveActive());
  if (typeof window !== "undefined") {
    window.dispatchEvent(new CustomEvent(EVENT_THEME, { detail: { name: pref } }));
    if (designCleared) {
      window.dispatchEvent(new CustomEvent(EVENT_DESIGN, { detail: { name: null } }));
    }
  }
}

export function getTheme(): ThemePreference {
  return activeTheme;
}

/** Set the design axis (the decorative palette overlay). The theme
 *  axis is unchanged — the user's brightness mode preference outlasts
 *  any design experiment. */
export function setDesign(d: Design) {
  if (d === activeDesign) return;
  activeDesign = d;
  try { localStorage.setItem(KEY_DESIGN, d); } catch { /* see above */ }
  applyAttr(resolveActive());
  if (typeof window !== "undefined") {
    window.dispatchEvent(new CustomEvent(EVENT_DESIGN, { detail: { name: d } }));
  }
}

/** Clear the design — chrome reverts to the theme axis underneath. */
export function clearDesign() {
  if (activeDesign === null) return;
  activeDesign = null;
  try { localStorage.removeItem(KEY_DESIGN); } catch { /* see above */ }
  applyAttr(resolveActive());
  if (typeof window !== "undefined") {
    window.dispatchEvent(new CustomEvent(EVENT_DESIGN, { detail: { name: null } }));
  }
}

export function getDesign(): Design | null {
  return activeDesign;
}

/** Resolved theme right now (system → light|dark; design > theme).
 *  Useful for code that needs to branch on the actual visual mode. */
export function getResolvedTheme(): ResolvedTheme {
  return resolveActive();
}

if (typeof window !== "undefined") {
  // Cross-tab/cross-frame propagation: storage events fire in OTHER
  // frames of the same origin when localStorage changes.
  window.addEventListener("storage", (e) => {
    if (e.key === KEY_THEME && isTheme(e.newValue) && e.newValue !== activeTheme) {
      activeTheme = e.newValue;
      applyAttr(resolveActive());
      window.dispatchEvent(new CustomEvent(EVENT_THEME, { detail: { name: e.newValue } }));
      return;
    }
    if (e.key === KEY_DESIGN) {
      const next = isDesign(e.newValue) ? e.newValue : null;
      if (next === activeDesign) return;
      activeDesign = next;
      applyAttr(resolveActive());
      window.dispatchEvent(new CustomEvent(EVENT_DESIGN, { detail: { name: next } }));
    }
  });

  // System-appearance changes propagate live when the theme axis is
  // "system" AND no design is overriding — flip dark↔light without a
  // refresh.
  if (window.matchMedia) {
    const mql = window.matchMedia("(prefers-color-scheme: dark)");
    const onChange = () => {
      if (activeTheme !== "system" || activeDesign !== null) return;
      applyAttr(resolveActive());
      window.dispatchEvent(new CustomEvent(EVENT_THEME, { detail: { name: activeTheme } }));
    };
    if (mql.addEventListener) mql.addEventListener("change", onChange);
    else if (mql.addListener) mql.addListener(onChange); // Safari < 14
  }
}

// Backwards-compat re-export so existing call sites that imported
// `ThemeName` keep compiling. New code should use ThemePreference.
export type ThemeName = ThemePreference;

/** Subscribe to theme-axis changes. Fires for explicit setTheme()
 *  calls and live OS appearance flips while "system" is selected.
 *  Returns an unsubscribe function. */
export function subscribeTheme(listener: (pref: ThemePreference) => void): () => void {
  if (typeof window === "undefined") return () => {};
  const handler = (e: Event) => {
    const detail = (e as CustomEvent<{ name?: ThemePreference }>).detail;
    if (detail?.name) listener(detail.name);
    else listener(activeTheme);
  };
  window.addEventListener(EVENT_THEME, handler);
  return () => window.removeEventListener(EVENT_THEME, handler);
}

/** Subscribe to design-axis changes. Fires for setDesign() and
 *  clearDesign() calls. The listener receives the new design, or null
 *  when cleared. */
export function subscribeDesign(listener: (d: Design | null) => void): () => void {
  if (typeof window === "undefined") return () => {};
  const handler = (e: Event) => {
    const detail = (e as CustomEvent<{ name?: Design | null }>).detail;
    listener(detail?.name ?? activeDesign);
  };
  window.addEventListener(EVENT_DESIGN, handler);
  return () => window.removeEventListener(EVENT_DESIGN, handler);
}
